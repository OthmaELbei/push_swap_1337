#include "push_swap.h"

int	ft_isdigit(char *str)
{
	int	i;

	i = 0;
	if ((((str[i] == '-') || (str[i] == '+'))
			&& ((str[i + 1] >= 48 && str[i + 1] <= 57))))
	{
		i++;
	}
	while (str[i])
	{
		if (!(str[i] >= 48 && str[i] <= 57))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
